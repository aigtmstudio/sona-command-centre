export const getDeal = (id: string) => deals.find((d) => d.id === id)
export const getDealsByAe = (aeId: string) => deals.filter((d) => d.assignedAe === aeId)
export const getDealsByStage = (stage: string) => deals.filter((d) => d.stage === stage)
export const getDealsByAccount = (accountId: string) => deals.filter((d) => d.accountId === accountId)
export const getActiveDeals = () => deals.filter((d) => !d.stage.startsWith('Closed'))
export const getTotalPipeline = () => getActiveDeals().reduce((sum, d) => sum + d.value, 0)
export const getWeightedPipeline = () => getActiveDeals().reduce((sum, d) => sum + d.value * (d.probability / 100), 0)
